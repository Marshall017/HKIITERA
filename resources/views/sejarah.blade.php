    Pelaksana Teknis (UPT) dan Pusat Riset yang potensi
    dalam pemenuhan persyaratan perlindungan KI sesuai
    peraturan perundangan yang berlaku, potensi komersial,
    dan potensi pengembangan ilmu pengetahuan dan
